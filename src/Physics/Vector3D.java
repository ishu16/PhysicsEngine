package Physics;

public class Vector3D {

    //------------------------------------------------------------------------------------------------------------------
    public float x = 0.0f;
    public float y = 0.0f;
    public float z = 0.0f;
    //------------------------------------------------------------------------------------------------------------------
    public Vector3D() {
    }
    public Vector3D(float x, float y, float z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }
    //------------------------------------------------------------------------------------------------------------------
}
